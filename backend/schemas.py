"""JSON Schemas for Upstage Universal Information Extraction - 6 NH credit review documents.

Upstage IE constraint: first-level properties must be string/number/integer/boolean/array
(no top-level objects). Nested groups are flattened with underscore-prefixed keys,
and repeating sections remain as arrays of objects.
"""


def _wrap(name: str, properties: dict) -> dict:
    return {
        "type": "json_schema",
        "json_schema": {
            "name": name,
            "schema": {
                "type": "object",
                "properties": properties,
            },
        },
    }


